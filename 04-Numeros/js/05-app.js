let puntaje = 5; 

puntaje++; // esto retorna 5 

++puntaje; // esto desde el inicio le suma 1 al valor que tenga puntaje

puntaje--; // retorna 5 
--puntaje; // enseguida retorna 4

// EJEMPLO DE INCREMENTOS MAYORES A 1 
 puntaje += 3; // esto hará un incremento de 3 en tres
 puntaje -= 3; // esto hará un decremento de 3 inmediato

console.log(puntaje);
